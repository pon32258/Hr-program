    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String value1 = jTextField1.getText();
        String value2 = jTextField2.getText();
        String value3 = jTextField3.getText();
        String value4 = jTextArea1.getText();
        String value5 = jTextField4.getText();
        String value6 = jTextField5.getText();

        Date stDate = Date.valueOf(value2);
        Date edDate = Date.valueOf(value3);
        int salary = Integer.parseInt(value5);

        try {
            Connection conn = ConnectionBuilder.getConnection();
            String sqlcmd = "INSERT INTO WorkHistory(Job_Title,StartDate,EndDate,"
                    + "Duty,Salary,EmployeeId,CompanyPreviousName) "
                    + "values(?,?,?,?,?,?,?)";
            PreparedStatement pstm = conn.prepareStatement(sqlcmd);
            pstm.setString(1, value1);
            pstm.setDate(2, stDate);
            pstm.setDate(3, edDate);
            pstm.setString(4, value4);
            pstm.setInt(5, salary);            
            pstm.setInt(6, Login.getUserId());
            pstm.setString(7, value6);

            if (pstm.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(null, "Complete");
                conn.close();
            }

        } catch (SQLException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error", "Warning", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

